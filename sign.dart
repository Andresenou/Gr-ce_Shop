class OtpWidget extends StatelessWidget {
  final List<TextEditingController> controllers;
  final List<FocusNode> focusNodes;
  final Function(String) onOtpEntered;
  final BuildContext context;

  OtpWidget({
    Key? key,
    required this.controllers,
    required this.focusNodes,
    required this.onOtpEntered,
    required this.context,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Column(
      children: [
        Text(
          "Entrez le code OTP",
          textAlign: TextAlign.center,
          style: TextStyle(
            fontSize: 24,
            color: Colors.black,
            fontWeight: FontWeight.bold,
          ),
        ),
        SizedBox(height: 20),
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceEvenly,
          children: List.generate(
            6,
                (index) => TweenAnimationBuilder(
              duration: Duration(milliseconds: 2000 + (index * 100)),
              tween: Tween<double>(begin: 0, end: 1),
              builder: (context, double value, child) {
                return Transform.scale(
                  scale: value,
                  child: Opacity(
                    opacity: value,
                    child: child,
                  ),
                );
              },
              child: _buildOTPField(controllers[index], focusNodes[index], index),
            ),
          ),
        ),
        SizedBox(height: 40),
        TweenAnimationBuilder(
          duration: Duration(milliseconds: 800),
          tween: Tween<double>(begin: 0, end: 1),
          builder: (context, double value, child) {
            return Transform.scale(
              scale: value,
              child: child,
            );
          },
          child: ElevatedButton(
            style: ElevatedButton.styleFrom(
              foregroundColor: Colors.white ,
              backgroundColor:  Colors.blueAccent ,
              shape: RoundedRectangleBorder(
                borderRadius: BorderRadius.circular(10),
              ),
            ),
            child: Text(
              "Valider",
              style: TextStyle(color: Colors.white, fontSize: 16),
            ),
            onPressed: () {
              String otp = controllers.map((controller) => controller.text).join();
              onOtpEntered(otp);
              Navigator.push(context, MaterialPageRoute(builder: (context) => ShoppingPage(),));
            },
          ),
        ),
      ],
    );
  }
  Widget _buildOTPField(TextEditingController controller, FocusNode focusNode, int index) {
    return Container(
      width: 40,
      child: AnimatedContainer(
        duration: Duration(milliseconds: 200),
        child: TextField(
          controller: controller,
          focusNode: focusNode,
          maxLength: 1,
          keyboardType: TextInputType.number,
          textAlign: TextAlign.center,
          decoration: InputDecoration(
            counterText: '',
            border: OutlineInputBorder(
              borderRadius: BorderRadius.circular(10),
              borderSide: BorderSide(color: Colors.blue),
            ),
            focusedBorder: OutlineInputBorder(
              borderRadius: BorderRadius.circular(10),
              borderSide: BorderSide(
                color: Colors.blueAccent,
                width: 2.0,
              ),
            ),
          ),
          onChanged: (value) {
            if (value.length == 1) {
              if (index < focusNodes.length - 1) {
                FocusScope.of(context).requestFocus(focusNodes[index + 1]);
              }
            } else if (value.isEmpty && index > 0) {
              FocusScope.of(context).requestFocus(focusNodes[index - 1]);
            }
          },
        ),
      ),
    );
  }
}